"""Main module."""


def shortbread(unit):
    """Prints ingredients for making `unit` dozen shortbread cookies."""
    print(f'{unit} cups flour')
    print(f'{.25 * unit} cups sugar')
    print(f'{.5 * unit} cups butter')
    print(f'Bake at 350 degrees for 10-12 minutes')
